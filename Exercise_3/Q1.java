package Exercise_3;

public class Q1 {

	public static void main(String[] args) {
		//  WAP to swap values of 2 numbers using third variable?//
		int a=14;
		int b=28;
		 
		System.out.println("Before swapping: a = "+ a + " ,b = " + b  );
		
		int c = a;
		a=b;
		b=c;
		System.out.println("After swapping : a = " + a + ", b= "+ b);

	}

}
